public interface OptionsUtilisateurs {

    /**
     * Ajoute un utilisateur à la liste d'utilisateur du programme.
     */
    void ajouter_utilisateur();

    /**
     * Enlève un utilisateur à la liste d'utilisateur du programme.
     */
    void supprimer_utilisateur();

    /**
     * Modifie un utilisateur de la liste d'utilisateur du programme.
     */
    void modifier_utilisateur();
}
